package helloworld.javaprograms;

public class myfirstprogram {

	    public static void main(String[] args) {
	        // Primitive data types
	        int anInt = 10;
	        double aDouble = 20.5;
	        char aChar = 'A';
	        boolean aBoolean = true;

	        // Wrapper classes
	        Integer integerObject = 100;
	        Double doubleObject = 200.5;
	        Character charObject = 'B';
	        Boolean booleanObject = false;

	        // Displaying the values
	        System.out.println("int: " + anInt);
	        System.out.println("double: " + aDouble);
	        System.out.println("char: " + aChar);
	        System.out.println("boolean: " + aBoolean);

	        System.out.println("Integer object: " + integerObject);
	        System.out.println("Double object: " + doubleObject);
	        System.out.println("Character object: " + charObject);
	        System.out.println("Boolean object: " + booleanObject);
	    }
	
}
