package com.example.myjavaprograms.operator;

public class IncrementDecrementExample3 {
    public static void main(String[] args) {
        int x;

        // Increment Examples
        x = 10;
        System.out.println("Increment Examples:");
        System.out.println(x);   
        System.out.println(++x); 
        System.out.println(x++); 
        System.out.println(x);   

        x = 22;
        System.out.println(x);   
        System.out.println(x++); 
        System.out.println(x);   

        // Decrement Examples
        x = 14;
        System.out.println("Decrement Examples:");
        System.out.println(x);   
        System.out.println(--x); 
        System.out.println(x--); 
        System.out.println(x);   

        x = 25;
        System.out.println(x);   
        System.out.println(x--); 
        System.out.println(x);   
    }
}
